import mongoose, { Schema, model } from 'mongoose'

const userSchema =  new mongoose.Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    age: {
        type: Number,
        required: true,
        min: 10
    },
    userName: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true,
        min: 8
    },
    bio: {
        type: String,
        default: ''
    },
    followers: {
        type: Array,
        default: []
    },
    following: {
        type: Array,
        default: []
    },
    profilePicture: {
        data: String,
        contentType: String
    },
    posts: [{type: mongoose.Types.ObjectId, ref: 'Posts'}]
    
}, {timestamps: true});

export default mongoose.model('User', userSchema)
